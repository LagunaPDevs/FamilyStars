import 'dart:core';

// This class defines all users

class User{
  String? id;
  String? name;
  String? parent;
  String? familiar;
  String? password;
  String? email;
  String? dob;
  String? stars;

  User({
    this.id,
    this.name,
    this.parent,
    this.familiar,
    this.password,
    this.email,
    this.dob,
    this.stars
  });
}
